console.log(myFunction(new Date(2019, 0, 1), new Date(2019, 0, 1)));      // 	true
console.log(myFunction(new Date('2020/01/01'), new Date('2020/01/02')));  // 	false
console.log(myFunction(new Date(2019, 0, 1), new Date(2019, 1, 1)));      // 	false
console.log(myFunction(new Date('2000/01/01'), new Date('2000/01/01')));  //	true
console.log(myFunction(new Date("2000/01/01"), new Date(2000, 00, 01)));  //  true


function myFunction(dateX, dateY) {
  return (dateX.toLocaleDateString("en-GB") == dateY.toLocaleDateString("en-GB") ? true : false);
  //return (Date.parse(dateY) == Date.parse(dateX) ? true : false); // SOLUCIÓN VÁLIDA
  return (dateX.toString() === dateY.toString() ? true : false);  // SOLUCIÓN VÁLIDA

}